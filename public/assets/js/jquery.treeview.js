;(function($) {

	// TODO rewrite as a widget, removing all the extra plugins
	$.extend($.fn, {
		swapClass: function(c1, c2) {
		},
		replaceClass: function(c1, c2) {
			return this.removeClass(c1).addClass(c2).end();
		},
		hoverClass: function(className) {

			className = className || "hover";

			return this.hover(function() {
				$(this).addClass(className);
			}, function() {
				$(this).removeClass(className);
			});
		},
		heightToggle: function(animated, callback) {
			animated ?
				this.animate({ height: "toggle" }, animated, callback) :
				this.each(function(){
					jQuery(this)[ jQuery(this).is(":hidden") ? "show" : "hide" ]();
					if(callback)
						callback.apply(this, arguments);
				});
		},
		heightHide: function(animated, callback) {
			if (animated) {
				this.animate({ height: "hide" }, animated, callback);
			} else {
				this.hide();
				if (callback)
					this.each(callback);				
			}
		},
		prepareBranches: function(settings) {
			return this.filter(":has(>ul)");
		},
		applyClasses: function(settings, toggler) {

			this.filter(":has(>ul):not(:has(>a))").find("span").unbind("click.treeview").bind("click.treeview", function(event) {
				// don't handle click events on children, eg. checkboxes

				if ( this == event.target ){
					var classList = this.classList;
					var len = classList.contains('icon-folder-close');

					if(classList.contains('icon-folder-close'))
						$(this).removeClass('icon-folder-close').addClass('icon-folder-open');
					else if(classList.contains('icon-folder-open'))
						$(this).removeClass('icon-folder-open').addClass('icon-folder-close');

					toggler.apply($(this).next());
				}
			}).add( $("a", this) ).hoverClass();
		},
		treeview: function(settings) {
			
			settings = $.extend({
				cookieId: "treeview"
			}, settings);
			
			if ( settings.toggle ) {
				var callback = settings.toggle;
				settings.toggle = function() {
					return callback.apply(this, arguments);
				};
			}
		
			// factory for treecontroller
			function treeController(tree, control) {
				// factory for click handlers
				function handler(filter) {
					return function() {
						// reuse toggle event handler, applying the elements to toggle
						// start searching for all hitareas
						toggler.apply( $("div." + CLASSES.hitarea, tree).filter(function() {
							// for plain toggle, no filter is provided, otherwise we need to check the parent element
							return filter ? $(this).parent("." + filter).length : true;
						}) );
						return false;
					};
				}
			}
		
			// handle toggle event
			function toggler() {
				this.swapClass( CLASSES.expandable, CLASSES.collapsable);

				$(this)
					.parent()
					// swap classes for parent li
					.find( ">ul" )
					// toggle them
					.heightToggle( settings.animated, settings.toggle );
			}
			this.data("toggler", toggler);
			
			function serialize() {
				function binary(arg) {
					return arg ? 1 : 0;
				}
				var data = [];
				branches.each(function(i, e) {
					data[i] = $(e).is(":has(>ul:visible)") ? 1 : 0;
				});
				$.cookie(settings.cookieId, data.join(""), settings.cookieOptions );
			}
			
			function deserialize() {
				var stored = $.cookie(settings.cookieId);
				if ( stored ) {
					var data = stored.split("");
					branches.each(function(i, e) {
						$(e).find(">ul")[ parseInt(data[i]) ? "show" : "hide" ]();
					});
				}
			}
			
			// add treeview class to activate styles
			this.addClass("treeview");
			
			// prepare branches and find all tree items with child lists
			var branches = this.find("li").prepareBranches(settings);

			branches.applyClasses(settings, toggler);


			return this;
		}
	});
	
	// classes used by the plugin
	// need to be styled via external stylesheet, see first example
	$.treeview = {};
	var CLASSES = ($.treeview.classes = {
		collapsable: "icon-folder-close",
		expandable: "icon-folder-open",
		last: "last"
	});
	
})(jQuery);